package ArrayProgramsAndILogical;

public class SumOfDigits {

    public static void main(String[] args) {

        int num=234;
        int sum=0;

        while(num!=0){
            int rem =num%10;
            sum=sum+rem;
            num=num/10;
        }
        System.out.println(sum);
        System.out.println(sum);
        System.out.println(sum);
        //new file
        //diff in files
    }

}
